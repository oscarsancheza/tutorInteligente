package tutorInteligente.models;

public class Ayuda {
  private int id;
  private String descripcion;

  public Ayuda() {}

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getDescripcion() {
    return descripcion;
  }

  public void setDescripcion(String descripcion) {
    this.descripcion = descripcion;
  }
}
